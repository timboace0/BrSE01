let input = Number(prompt("Nhập số từ 0 - 9: "));

switch(input){
    case 0:
        alert("Số Không");
    break;
    case 1:
        alert("Số Một");
    break;
    case 2:
        alert("Số Hai");
    break;
    case 3:
        alert("Số Ba");
    break;
    case 4:
        alert("Số Bốn");
    break;
    case 5:
        alert("Số Năm");
    break;
    case 6:
        alert("Số Sáu");
    break;
    case 7:
        alert("Số Bảy");
    break;
    case 8:
        alert("Số Tám");
    break;
    case 9:
        alert("Số Chín");
    break;
    default:
        alert("Số nằm ngoài thư viện của chúng tôi");
        break;
}